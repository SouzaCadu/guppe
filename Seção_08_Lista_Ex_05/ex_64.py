"""
implemente uma função que receba duas strings e concatene ambas
"""


def concatena_strings(s1, s2):
    """
    Receba duas strings e concatene ambas
    :param s1: Qualquer string
    :param s2: Qualquer string
    :return: Concatene ambas
    """

    return s1 + s2


s1 = "Recebe uma string"
s2 = "Recebe outra string"
print(concatena_strings(s1, s2))